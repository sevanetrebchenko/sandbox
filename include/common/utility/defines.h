
#pragma once

#include <cstdint>

namespace Sandbox {

    typedef std::uint8_t  u8;  // unsigned char
    typedef std::uint16_t u16; // unsigned short
    typedef std::uint32_t u32; // unsigned int
    typedef std::uint64_t u64; // unsigned long long

    typedef std::int8_t  i8;  // signed char
    typedef std::int16_t i16; // signed short
    typedef std::int32_t i32; // signed int
    typedef std::int64_t i64; // signed long long

    typedef std::float_t  f32; // float
    typedef std::double_t f64; // double

    typedef bool b8; // bool

}